                                            <p>
                                                Sistema de governança é o modo como os diversos atores se organizam, interagem e procedem para obter boa governança. Compreende as instâncias internas e externas de governança, fluxo de informações, processos de trabalho e atividades relacionadas a avaliação, direcionamento e monitoramento.
                                            </p>
                                            <p>O alcance de uma boa governança depende fundamentalmente da definição e implantação de um sistema de governança ao mesmo tempo simples e robusto, com poderes de decisão balanceados e funções críticas segregadas.</p>
                                            <p>Para dar esse passo é importante estabelecer as instâncias internas de governança da organização; garantir o balanceamento de poder e a segregação de funções críticas; estabelecer o sistema de governança da organização e divulgá-lo para as partes interessadas.</p>
