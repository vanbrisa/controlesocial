                                                A conduta ética deve estar sempre presente na instituição pública, devendo as organizações incentivarem, encorajarem e reforçarem o comportamento ético das lideranças e seus agentes. Devendo ser demonstrado sempre que as ações estão sempre em conformidade com os interesses da sociedade. 
                                            </p>
                                            <p>Para viabilizar esse passo, é importante adotar um código de ética e conduta que se aplique à alta administração, adotar mecanismos de controle para evitar que preconceitos, vieses ou conflitos de interesse influenciem as decisões e as ações dos membros da alta administração, adotar mecanismos para garantir que a alta administração atue de acordo com padrões de comportamento baseados nos valores e princípios constitucionais, legais e organizacionais e no código de ética e conduta adotado.</p>
