                                            <p>
                                                Considerando o necessário foco das organizações públicas na prestação de serviços de qualidade, o alinhamento de suas ações com as expectativas dos usuários e demais partes interessadas é fundamental para a otimização dos resultados. </p>

                                                <p>De forma geral, os gestores precisam satisfazer uma gama complexa de objetivos políticos, econômicos e sociais. Logo, o modelo de governança adotado deve propiciar o equilíbrio entre as legítimas expectativas das diferentes partes interessadas. </p>

                                                <p>Para viabilizar esse passo é importante estabelecer e divulgar canais de comunicação com as diferentes partes interessadas e assegurar sua efetividade; promover a participação social, com envolvimento dos usuários, da sociedade e das demais partes interessadas na governança da organização; estabelecer relação objetiva e profissional com a mídia, organizações de controle e outras organizações; assegurar que decisões, estratégias, políticas, programas, projetos, planos, ações, serviços e produtos atendam ao maior número possível de partes interessadas, de modo balanceado.</p>
                                        </div>
                                    </div>
                                </div>

