                                                O papel fundamental atribuído às organizações públicas é o de ampliar, de forma sistêmica e integrada, o bem estar social e as oportunidades aos cidadãos. A organização, a partir de sua visão de futuro, da análise dos ambientes interno e externo e da sua missão institucional, deve formular suas estratégias, desdobrá-las em planos de ação e acompanhar sua implementação, oferecendo os meios necessários ao alcance dos objetivos institucionais e à maximização dos resultados. </p>

                                                
                                                <p>Para dar esse passo é importante estabelecer um modelo de gestão da estratégia que considere aspectos como transparência e envolvimento das partes interessadas; estabelecer a estratégia da organização; monitorar e avaliar a execução da estratégia, os principais indicadores e o desempenho da organização.
                                            </p>
