                                            <p>
                                                Riscos surgem da incerteza natural dos atuais cenários econômico, político e social e podem se apresentar como desafios ou oportunidades, na medida em que dificultem ou facilitem o alcance dos objetivos organizacionais. O instrumento de governança para lidar com a incerteza é a gestão de riscos, que engloba, entre outras coisas, os controles internos. </p>

                                                <p>A gestão de riscos permite tratar com eficiência as incertezas, seja pelo melhor aproveitamento das oportunidades, seja pela redução da probabilidade e/ou impacto de eventos negativos, a fim de melhorar a capacidade de gerar valor e fornecer garantia razoável do cumprimento dos seus objetivos. </p>

                                                <p>Para viabilizar esse passo é importante estabelecer sistema de gestão de riscos; monitorar e avaliar o sistema de gestão de riscos, a fim de assegurar que seja eficaz e contribua para a melhoria do desempenho organizacional.</p>
