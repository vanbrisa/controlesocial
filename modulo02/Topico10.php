                                                <p>Constitui uma forma de dotar o gestor de uma rede de informações e instrumentos de suporte à tomada de decisão. Ademais, a atuação efetiva da auditoria interna auxilia na prevenção de desvios que poderiam ensejar apontamentos de irregularidades pelo Tribunal de Contas, com a consequente responsabilização dos agentes. Para dar esse passo estabeleça a função de auditoria interna; crie condições para que a auditoria interna seja independente e proficiente; e assegure que a auditoria interna adicione valor à organização. </p>
