                            <!-- Título -->
                            <h2 class="Titulo">10 - Governança</h2>
                            <hr>

                            <p>Segundo a Comunidade da Nações Unidas (ONU), um governo é considerado bom e democrático se as instituições e os processos forem transparentes. Os processos realizados pelas instituições devem estar livres da corrupção e prestar contas à população. A ONU também afirma que para o um país alcançar o sucesso o padrão de governança é uma medida chave de credibilidade e respeito no mundo.</p>
                            <p>A boa governança é capaz de promover a igualdade, a participação, o pluralismo, a transparência, a responsabilidade e o Estado de Direito, de forma efetiva, eficiente e duradoura.</p>

                            <!-- CITAÇÃO -->
                            <div class="bd-callout bd-callout-success">
                                <p class="Texto">
                                    Segundo o Tribunal de Contas da União, governança pode ser descrita como um sistema pelo qual as organizações são dirigidas, monitoradas e incentivadas, envolvendo os relacionamentos entre <strong>sociedade, alta administração, servidores ou colaboradores e órgãos de controle.</strong>
                                </p>
                            </div>

                            <p>Em essência, a boa governança pública tem como propósitos conquistar e preservar a confiança da sociedade, por meio de conjunto eficiente de mecanismos, a fim de assegurar que as ações executadas estejam sempre alinhadas ao interesse público.</p>
                            <p>Dessa forma, governança pública refere-se a mecanismos de avaliação, direção e monitoramento, a forma que as organizações se relacionam com o cidadão, como são tomadas decisões e como o poder e a responsabilidade são exercidos. Alguns mecanismos precisam ser adotados na boa governança, especialmente liderança, estratégia e controle.</p>

                            <ul class="none">
                                <li><i class="fa fa-dot-circle CorVerdeClaroEstado"></i> <strong>Liderança:</strong> refere-se ao conjunto de práticas de natureza humana ou comportamental que asseguram a existência das condições mínimas para o exercício da boa governança.</li>
                                <li><i class="fa fa-dot-circle CorVerdeClaroEstado"></i> <strong>Estratégia:</strong> envolve o relacionamento com partes interessadas, a definição e monitoramento de objetivos, indicadores e metas, bem como o alinhamento entre planos e operações de unidades e organizações envolvidas na sua execução.</li>
                                <li><i class="fa fa-dot-circle CorVerdeClaroEstado"></i> <strong>Controle:</strong> abrange aspectos como transparência, prestação de contas e responsabilização.</li>
                            </ul>

                            <p>Os princípios básicos que norteiam as boas práticas de governança nas organizações públicas, segundo o International Federation of Accountants (IFAC), são: <u>transparência, integridade, prestação de contas, liderança, compromisso e integração</u>. Assim, segundo o IFAC, a boa governança do setor público permite:</p>

                            <ol type="a">
                                <li>Garantir a entrega de benefícios econômicos, sociais e ambientais para os cidadãos;</li>
                                <li>Garantir que a organização seja, e pareça, responsável para com os cidadãos;</li>
                                <li>Ter clareza acerca de quais são os produtos e serviços efetivamente prestados para cidadãos e usuários, e manter o foco nesse propósito;</li>
                                <li>Ser transparente, mantendo a sociedade informada acerca das decisões tomadas e dos riscos envolvidos;</li>
                                <li>Possuir e utilizar informações de qualidade e mecanismos robustos de apoio às tomadas de decisão;</li>
                                <li>Dialogar com e prestar contas à sociedade;</li>
                                <li>Garantir a qualidade e a efetividade dos serviços prestados aos cidadãos;</li>
                                <li>Promover o desenvolvimento contínuo da liderança e dos colaboradores;</li>
                                <li>Definir claramente processos, papéis, responsabilidades e limites de poder e de autoridade;</li>
                                <li>Institucionalizar estruturas adequadas de governança;</li>
                                <li>Selecionar a liderança tendo por base aspectos como conhecimento, habilidades e atitudes (competências individuais);</li>
                                <li>Avaliar o desempenho e a conformidade da organização e da liderança, mantendo um balanceamento adequado entre eles;</li>
                                <li>Garantir a existência de um sistema efetivo de gestão de riscos;</li>
                                <li>Utilizar-se de controles internos para manter os riscos em níveis adequados e aceitáveis;</li>
                                <li>Controlar as finanças de forma atenta, robusta e responsável; e</li>
                                <li>Prover aos cidadãos dados e informações de qualidade (confiáveis, tempestivas, relevantes e compreensíveis).</li>
                            </ol>

                            <p>Em 2014, o Tribunal de Contas da União (TCU) emitiu um documento em que indica os 10 passos para a boa governança, dirigido aos líderes dos municípios, estados, Distrito federal e União, com o objetivo de contribuir para a melhoria da governança pública e possamos superar o desafio de transformar o “país do futuro”, como somos conhecidos há anos, no “país do presente”.</p>
                            <p>Os 10 passos elencados pelo TCU são os seguintes:</p>

