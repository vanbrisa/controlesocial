                                                <p>Do contrário, abre-se espaço para a fragmentação da missão e a sobreposição de programas, com o consequente desperdício de recursos públicos.</p>

                                                <p>Para dar esse passo é importante estabelecer mecanismos de atuação conjunta com vistas a formulação, implementação, monitoramento e avaliação de políticas públicas transversais, multidisciplinares e/ou descentralizadas.
                                            </p>
