                                                <p><em>Accountability</em> refere-se à obrigação que têm aqueles a quem foram confiados recursos de assumir as responsabilidades de ordem fiscal, gerencial e programática que lhes foram conferidas, e de informar a quem lhes delegou essas responsabilidades. Assim como a transparência, consiste em valor democrático, consequência direta da delegação do poder soberano do povo aos governantes. </p>

                                                <p>Para viabilizar esse passo dê transparência da organização às partes interessadas, admitindo-se o sigilo, como exceção, nos termos da lei; preste contas da implementação e dos resultados dos sistemas de governança e de gestão, de acordo com a legislação vigente e com o princípio de accountability; avalie a imagem da organização e a satisfação das partes interessadas com seus serviços e produtos; garanta que indícios de irregularidades sejam apurados de ofício, promovendo a responsabilização em caso de comprovação.</p>
