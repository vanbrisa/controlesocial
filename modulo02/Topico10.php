                                            <p>
                                                O modelo de liderança organizacional decorre da aplicação dos princípios da coordenação, da delegação de competência e do modelo de governança adotado. Por esses princípios fundamentais, a Alta Administração estabelece uma estrutura de unidades e subunidades funcionais, nomeia gestores para chefiá-las e a eles delega autoridade (mandato legal e poder sobre os recursos alocados) para executar os planos. A responsabilidade final pelos resultados, contudo, permanece com a autoridade delegante. Para dar esse passo, é importante avaliar, direcionar e monitorar a gestão; responsabilizar-se pelo estabelecimento de políticas e diretrizes para a gestão e pelo alcance dos resultados; assegurar, por meio de política de delegação e reserva de poderes, </p>

                                                <p>A capacidade das instâncias internas de governança de avaliar, direcionar e monitorar a organização; responsabilizar-se pela gestão de riscos; avaliar os resultados das atividades de controle e dos trabalhos de auditoria e, se necessário, determinar que sejam adotadas providências.
                                            </p>
                                        </div>
                                    </div>
