                            <p class="SubTitulo">04.1 - Pedidos de Informação Pública</p>
                            <hr>

                            <p>Em tópicos anteriores já trabalhamos o direito fundamental de acesso à informação pública além de alguns aspectos importantes na identificação da informação púbica. Agora, passaremos ao estudo do pedido de acesso à informação e suas particularidades.</p>

                            <br><!-- Sub Sub Título -->
                            <p class="SubSubTitulo">04.1.1 - Quem Pode Pedir Acesso à Informação</p>
                            <hr>

                            <p>No que concerne ao acesso à informação, a LAI estadual (art. 12) afirma que o pedido não precisa ser justificado, devendo conter apenas a identificação do requerente e a especificação da informação solicitada. Pessoas físicas, independente de idade ou da nacionalidade, assim como pessoas jurídicas, prontamente identificadas, podem solicitar informação públicas junto aos órgãos/entidades do Estado.</p>

                            <br><!-- Sub Sub Título -->
                            <p class="SubSubTitulo">04.1.2 - O Que Não é Um Pedido de Acesso à Informação</p>
                            <hr>

                            <p>Ao longo dos últimos anos, com a facilidade dos sistemas de informação e o interesse crescente da sociedade nos assuntos da gestão pública, cada vez mais aparecem demandas das mais variadas espécies. Assim, ora estamos diante de um pedido de serviço, ora uma reclamação, ora um denúncia, ora uma solicitação de informação e, não raro, a combinação, em uma mesma demanda, de mais de uma espécie de pedido. Nesse sentido, se faz necessário o preparo do agente público para a precisa identificação da natureza da demanda o que permitirá a realização das medidas adequadas à satisfação do cidadão.  </p>
                            <p>Podemos afirmar que um <strong>pedido de acesso à informação</strong> constitui-se em uma demanda encaminhada ao Poder Público (órgãos e entidades), por qualquer pessoa, física ou jurídica, que <strong>tenha por objeto um dado ou informação</strong>. Essa última (informação) encontra-se conceituada no inciso I do art. 4° da Lei de Acesso à Informação Federal, nos seguintes termos:</p>

                            <!-- CITAÇÃO -->
                            <div class="bd-callout bd-callout-success">
                                <p>Art. 4º Para os efeitos desta Lei, considera-se: </p>
                                <p>I - informação: dados, processados ou não, que podem ser utilizados para produção e transmissão de conhecimento, contidos em qualquer meio, suporte ou formato;</p>
                                <p>[...]</p>
                            </div>


                            <p>Por vezes, como alertado antes, os pedidos de acesso à informação podem ser <strong>confundidos</strong> com a solicitação de uma <strong>prestação de serviço</strong>, em outras vezes podem ser formulados conjuntamente com outras demandas tais como, <strong>reclamações, denúncias, consultas jurídicas</strong>. Nesses casos, faz-se necessário a precisa compreensão do objeto de uma solicitação de informação e a separação para o atendimento eficaz do cidadão. </p>

