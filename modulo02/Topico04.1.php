                                                Um pedido desarrazoado é aquele que não encontra amparo para a sua concessão nos termos dos objetivos da legislação de acesso à informação e, tampouco, nas garantias fundamentais previstas na Constituição. É um pedido que se caracteriza pela desconformidade com os interesses públicos do Estado em prol da sociedade, como a segurança pública, a celeridade e a economicidade da administração pública. 
                                            </p>
                                            <p>A título de exemplo, a doutrina tem apontado a solicitação de plantas de presídios. As informações contidas nesses documentos, mesmo não estando classificadas, deverá ser negado por afronta ao princípio da razoabilidade.</p>
                                            
