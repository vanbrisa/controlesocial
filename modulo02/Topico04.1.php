                                            <p>
                                                Um outro exemplo de pedido de informação que se afasta das finalidades da legislação de acesso à informação diz respeito ao pedido de acesso desproporcional. Aqui, deve ser dito que a informação, para ser fornecida, também não deve comprometer, significativamente, a realização das atividades rotineiras da instituição requerida, acarretando prejuízo injustificado aos direitos de outros solicitantes.  </p>

                                                <p>Para a adequada caracterização da desproporcionalidade do pedido, é imprescindível que o órgão, ao responder o pedido inicial, indique ao cidadão de forma clara e concreta que o seu pedido inviabilizaria a rotina da unidade responsável pela produção da resposta. O que inviabiliza a sua entrega, portanto, é a dificuldade operacional em se organizar a informação, e não o seu conteúdo.</p>

                                                <p><strong>a. Pedido que exige um trabalho adicional de análise, interpretação de dados ou serviço que não seja da competência do órgão ou entidade</strong></p><hr>
                                                
                                                <p>Nestas situações o que está em questão são aspectos relacionados a transferência de trabalho para a administração pública que, em regra, deveria ser executado pelo próprio requerente. Essas situações estão previstas no inciso III do art. 13 do Decreto n° 7.724/12 refere-se as seguintes situações:</p>

                                                <ul class="none">
                                                    <li><i class="fa fa-dot-circle CorVerdeClaroEstado"></i> Pedidos de acesso à informação que exijam trabalhos adicionais de análise, interpretação ou consolidação de dados e informações; e</li>
                                                    <li><i class="fa fa-dot-circle CorVerdeClaroEstado"></i> Pedidos de acesso à informação que exijam serviço de produção ou tratamento de dados que não seja de competência do órgão ou entidade.</li>
                                                </ul>

                                                <p>A primeira hipótese ocorre quando o órgão ou a entidade pública possui as informações solicitadas, mas elas não estão dispostas nos moldes pretendidos pelo cidadão. Diversos aspectos podem configurar a divergência entre a pretensão do solicitante e a forma como a instituição dispõe da informação. Um exemplo dessa situação é o caso em que o cidadão demanda a organização dos dados em modelo, software ou indicadores não existentes no órgão ou entidade pública.</p>
                                                <p>No segundo caso, pode-se indicar a hipótese em que a resposta ao pedido demande atividades (como a coleta, o agrupamento e a análise de documentos) que não estejam dentre as competências da instituição. Dessa maneira, caso a produção da informação demandada pelo cidadão não seja de competência do órgão requerido - ou este não a tenha sob sua posse -, o Serviço de Informações ao Cidadão deste órgão não está obrigado legalmente a fornecê-la. </p>
                                                <p>Nas duas hipóteses, acima tratadas, o órgão ou a entidade pública deve indicar o local onde se encontram as informações demandadas, de maneira que o cidadão possa, ele próprio, realizar o trabalho de análise, interpretação e consolidação dos dados. </p>

                                                <p><strong>b. O Pedido de informação com procedimento administrativo específico</strong></p><hr>

                                                <p>Existem casos em que a Administração Pública reconhece, na informação, a importância e a intensidade de sua solicitação, criando um procedimento específico por meio do qual a informação será entregue ao cidadão. Assim, em casos de requerimento de tais informações, o Poder Público deverá orientar o cidadão a buscar as informações por meio desse canal, indicando os prazos e as condições para a sua obtenção. Estes procedimentos deverão prevalecer sempre que existentes e efetivos, em respeito ao princípio administrativo da eficiência e economicidade. Esse entendimento encontra-se exarado na Súmula CMRI nº 1/2015, nos seguintes termos:</p>

                                                <!-- CITAÇÃO -->
                                                <div class="bd-callout bd-callout-success">
                                                    <p class="Texto">
                                                        <strong>PROCEDIMENTO ESPECÍFICO</strong> – Caso exista canal ou procedimento específico efetivo para obtenção da informação solicitada, o órgão ou a entidade deve orientar o interessado a buscar a informação por intermédio desse canal ou procedimento, indicando os prazos e as condições para sua utilização, sendo o pedido considerado atendido.<p class="text-end">(SÚMULA CRMI, 2015, online)</p>
                                                    </p>
