                                <!-- Título -->
                                <h2 class="Titulo">01 - Controle: Definições</h2>
                                <hr>

                                <p>
                                    O controle, numa perspectiva da ciência da Administração, é uma função básica administrativa, com a finalidade de garantir que o que foi planejado seja executado da melhor maneira possível, de modo que os objetivos traçados possam ser alcançados por uma organização. Para Chiavenato:
                                </p>

                                <!-- CITAÇÃO -->
                                <div class="bd-callout bd-callout-success">
                                    <p class="Texto">
                                        (...) O controle é o processo pelo qual são fornecidas as informações e retroação para manter as funções dentro de suas respectivas trilhas. É a atividade integrada e monitorada que aumenta a probabilidade de que os resultados planejados sejam atingidos da melhor maneira. <p class="text-end">(CHIAVENATO, Idalberto. 2008, p. 376)</p>
                                    </p>
                                </div>

                                    <p><br>
                                        No âmbito da administração pública, o controle abrange os órgãos do poder executivo, legislativo e judiciário, e está sistematizado em vários diplomas legais do ordenamento jurídico. Na lição do Professor Hely Lopes Meirelles (2010, p. 697) “controle é a faculdade de vigilância, orientação e correção que um Poder, órgão ou autoridade exerce sobre a conduta funcional de outro”. Marcelo Alexandrino e Vicente Paulo conceituam o controle da administração pública como:
                                    </p>

                                    <!-- CITAÇÃO -->
                                    <div class="bd-callout bd-callout-success">
                                        <p class="Texto">
                                            (...) O conjunto de instrumentos que o ordenamento jurídico estabelece a fim de que a administração pública, os Poderes Judiciário e Legislativo <strong>e ainda o povo, diretamente ou por meio de órgãos especializados, possam exercer o poder de fiscalização (grifo nosso)</strong>, orientação e revisão da atuação administrativa de todos os órgãos, entidades e agentes públicos, em todas as esferas de Poder.  <p class="text-end">(ALEXANDRINO, Marcelo & PAULO, Vicente. 2010, p.758 e 759)</p>
                                        </p>
                                    </div>



                                        <p><br>
                                            A importância do controle no ordenamento jurídico brasileiro passou a ser destacada, principalmente, com a <strong>Reforma Administrativa de 1967</strong>, conforme o artigo 6º do <strong>Decreto-lei nº. 200/67</strong>, que coloca o controle, assim como o planejamento, entre os cinco princípios fundamentais que norteiam a atividade pública federal. O artigo 13 traz suas principais competências:
                                        </p>

                                        <!-- HR LATERAL AZUL -->
                                        <div class="bd-callout bd-callout-primary">
                                            <p>
                                                <strong>Art. 13:</strong> O controle das atividades da Administração Federal deverá exercer-se em todos os níveis e em todos os órgãos, compreendendo, particularmente:
                                                    <ol type="a">
                                                        <li> O controle, pela chefia competente, da execução dos programas e da observância das normas que governam a atividade específica do órgão controlado;</li>
                                                        <li> O controle, pelos órgãos próprios de cada sistema, da observância das normas gerais que regulam o exercício das atividades auxiliares;</li>
                                                        <li> O controle da aplicação dos dinheiros públicos e da guarda dos bens da União pelos órgãos próprios do sistema de contabilidade e auditoria.</li>
                                                    </ol>
                                                
                                            </p>
                                        </div>



                                        <p class="Texto"><br>
                                            Obviamente, existem limitações inerentes à execução do controle, de modo que não existe garantia absoluta que os objetivos da entidade serão alcançados, dentre elas podemos citar: o custo de manter um controle em relação ao seu benefício, pois, na prática, manter um controle mais caro que o benefício gerado seria inexeqüível e impraticável. É nesse sentido a redação do artigo 14, do <strong>Decreto-lei 200/1967</strong>:
                                        </p>

                                        <!-- CITAÇÃO -->
                                        <div class="bd-callout bd-callout-success">
                                            <p>
                                                <strong>Art. 14.</strong> O trabalho administrativo será racionalizado mediante simplificação de processos e supressão de controles que se evidenciarem como puramente formais ou cujo custo seja evidentemente superior ao risco.
                                            </p>
                                        </div>



                                            <p>
                                                Eventos externos imprevisíveis, como catástrofes naturais ou crises financeiras, também estão além do controle de qualquer organização.
                                                <br><br> Nesse sentido, vislumbram-se vários tipos de controle: o controle hierárquico de uma chefia ou órgão específico, o controle dos recursos, bem como o controle de conformidade com as normas. A <strong>Constituição Federal</strong> emprega ainda o termo controle sob várias acepções:
                                            </p>

                                            <ul  class="none">
                                                <li class="img-fluid wow fadeInLeft" data-wow-delay="0.3s"><i class="fa fa-arrow-right CorVerdeClaroEstado"></i> “Controle externo e controle interno dos Municípios” (art. 31);</li>
                                                <li class="img-fluid wow fadeInLeft" data-wow-delay="0.6s"><i class="fa fa-arrow-right CorVerdeClaroEstado"></i> “Controles dos contratos de gestão” (art. 37, §8º, II);</li>
                                                <li class="img-fluid wow fadeInLeft" data-wow-delay="0.9s"><i class="fa fa-arrow-right CorVerdeClaroEstado"></i> “Controle pelo Congresso Nacional dos atos do Poder Executivo” (art. 49, X);</li>
                                                <li class="img-fluid wow fadeInLeft" data-wow-delay="1.0s"><i class="fa fa-arrow-right CorVerdeClaroEstado"></i> “Controle externo e controle interno da União” (arts. 70 a 75);</li>
                                                <li class="img-fluid wow fadeInLeft" data-wow-delay="1.3s"><i class="fa fa-arrow-right CorVerdeClaroEstado"></i> “Controle da atuação administrativa e financeira do Poder Judiciário pelo Conselho Nacional de Justiça” (art. 103-B, §4º);</li>
                                                <li class="img-fluid wow fadeInLeft" data-wow-delay="1.6s"><i class="fa fa-arrow-right CorVerdeClaroEstado"></i> “Controle externo da atividade policial pelo Ministério Público” (art. 129, VII);</li>
                                                <li class="img-fluid wow fadeInLeft" data-wow-delay="1.9s"><i class="fa fa-arrow-right CorVerdeClaroEstado"></i> “Controle das ações e serviços de saúde” (art. 197);</li>
                                            </ul>

                                            <p>
                                                Sopesando os diferentes significados do termo, a doutrina costuma dividir as classificações de controle sob vários aspectos, dos quais podemos citar:
                                            </p>

