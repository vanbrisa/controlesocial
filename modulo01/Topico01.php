                                                            <p class="Texto">
                                                                O controle é interno quando exercido pela entidade ou órgão que é o responsável pela atividade controlada, inclusive em grau hierárquico, no âmbito de sua própria estrutura. No mesmo sentido, existe ainda o controle interno por meio de órgãos especializados, sem relação de hierarquia com o órgão controlado, mas dentro da estrutura <strong>de um mesmo poder</strong>, a exemplo da CGU no Poder Executivo Federal e das Controladorias Estaduais, CGE's, nos Poderes Executivos Estaduais.
                                                                <br><br>O controle externo (político, contábil, financeiro, orçamentário, operacional e patrimonial) é exercido pelo Congresso Nacional (Assembléias Legislativas nos estados) com o auxílio do Tribunal de Contas. Pode-se citar ainda como um tipo de controle externo, o controle jurisdicional realizado pelo poder judiciário em relação aos atos da administração pública.
                                                                <br><br>O texto Constitucional prevê como finalidade do sistema de controle interno o apoio ao controle externo no exercício de sua missão institucional, configurando, portanto, um estreito relacionamento na atuação de ambos, sem existir, entretanto, relação de hierarquia.
