                                    Quanto mais avançado o estágio democrático, maior o interesse pela accountability. E a accountability governamental tende a acompanhar o avanço de valores democráticos, tais como <strong>igualdade, dignidade humana, participação, representatividade</strong>.</p>
                                </p>
                            </div>

                             <p class="Texto"><br>O agente administra bens pertencentes ao principal, por isso <strong>deve prestar contas</strong> desta administração. O segundo aspecto é a responsabilização, pois o agente <strong>deve responder</strong> em termos de legalidade e em termos de eficiência, eficácia e efetividade pelos atos praticados em nome do principal. A <strong>responsividade</strong> refere-se à propensão do agente em atuar conforme à vontade do principal. O agente deve tomar suas decisões de forma a maximizar os interesses do principal, e não os seus próprios interesses.</p>

                             <!-- CITAÇÃO -->
                             <div class="bd-callout bd-callout-success">
                                <p class="Texto">
                                    Quando as atividades governamentais se expandem e aumenta a intervenção do governo na vida do cidadão, a preservação dos direitos democráticos requer necessariamente a expansão dos limites da arena em que se exerce o controle. O problema do controle assume, de fato, uma dimensão de legitimidade. Quem controla o controlador? Pode essa tarefa ficar nas mãos do Estado? Em termos ideais, tal controle constituiria prerrogativa essencial dos cidadãos: não dos cidadãos individualmente, mas da cidadania organizada. Isso porque, a despeito de sua legitimidade, as reivindicações individuais não dispõem da força necessária para conter o abuso do poder por parte do governo. O ponto a enfatizar, mais uma vez, é que um controle efetivo é conseqüência da cidadania organizada; uma sociedade desmobilizada não será capaz de garantir a accountability.  <p class="text-end">(CAMPOS, 1990)</p>
                                </p>
