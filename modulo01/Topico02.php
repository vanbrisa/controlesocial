                            <br><p class="Texto">Nessa perspectiva, é possível idealizar a relação entre o Estado (<strong>agente</strong>) e o povo (<strong>principal</strong>) na delegação por parte deste último pela gestão de seu patrimônio. O Estado deve atuar de acordo com o interesse público seguindo a máxima da impessoalidade. Segundo Anna Maria Campos:</p>

                            <!-- CITAÇÃO -->
