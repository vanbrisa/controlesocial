                            <h2 class="Titulo">02 - <em>Accountability</em></h2>
                            <hr>


                            <p>Antes de adentrarmos aos mecanismos de controle social, vamos pontuar alguns deveres que o Estado (lato sensu) possui, destacando inicialmente a necessidade de <strong>justificação</strong> e de <strong>informação</strong> de determinados atos. </p>
                            <p>A teoria da agência busca explicar a relação entre um agente e um principal a partir de atos e dos interesses (muitas vezes conflitantes) que podem decorrer desse vínculo. O termo <strong>accountability</strong> indica o dever do <strong>agente</strong> em desempenhar suas atividades em nome do <strong>principal</strong>, com as responsabilidades inerentes quando se administra bens e direitos de um terceiro, e como o principal pode realizar o controle das ações desempenhadas pelo agente.</p>

                            <!-- Importante -->
